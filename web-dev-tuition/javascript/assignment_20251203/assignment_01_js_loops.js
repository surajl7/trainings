// While Loop
console.log("While Loop Example: ")
var i = 11;
while(i<=15)
{
    console.log(i)
    i++;
}

// =====================================================================================================

// Do While loop
console.log("Do While Loop Example: ") // To create an infinite Loop
var i = 21;
do{
    console.log(i)
}
while(i<=25);